/*
 * Which statement is true about the switch statement?
 * a)
 * It must contain the default section.
 * 
 * b)
 * The break statement, at the end of each case block, is optional.
 * 
 * c)
 * Its case label literals can be changed at runtime.
 * 
 * d)
 * Its expression must evaluate to a collection of values.
 * 
 * Answer : 
 * b)
 * The break statement, at the end of each case block, is optional.
 * 
 * Explanation: 
 * This is a classic example of applying process of elimination. Default section is optional and is called when value does not match any of the cases, Hence option a is incorrect. 
 * The literals in each case statement must be compile-time constant values of the same datatype as the switch value. Hence option c is also incorrect.
 * And it is no necessary for the expression to evaluate a collection of values. Hence option d is also incorrect. Therefore option b is the only correct answer.
 * 
 * Additional Explanation: 
 * Refer the book Jeanne Boyarsky, Scott Selikoff - Study Guide.
 * 
 * Source : 
 * Actual TEst 2
*/